    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {
        
        var login = loginTextField.getText();
        var senha = new String(senhaPasswordField.getPassword());
        
        //definir comando SQL
        var sql = "select * from tb_login where login='"+login+"' and senha='"+senha+"'";
        
        //abrir conex√£o como recurso do try
        try (Connection c = ConnectionFactory.obtemConexao()){

            Statement statement = c.createStatement();
            ResultSet auth = statement.executeQuery(sql);
            //pre compilar o comando SQL
            //PreparedStatement ps = c.prepareStatement(sql);
            //preencher com valores o statement SQL
            //ps.setString(1, login);
            //ps.setString(2, senha);
            //executa o comando
            //var auth = ps.executeQuery(sql);
            
            if(auth.next()){
                if(auth.getInt(5) == 1){
                    this.dispose();
                    JOptionPane.showMessageDialog(null, "Bem vindo administrador!");
                    new TelaAdmin().setVisible(true);
                } else {
                    this.dispose();
                    JOptionPane.showMessageDialog(null, "Bem vindo!");
                    new TelaUsuario().setVisible(true);
                }
                
            } else {
                JOptionPane.showMessageDialog(null, "Usuario ou senha incorretos!");
                loginTextField.setText("");
                senhaPasswordField.setText("");
            }
        }
        catch (Exception e){
            e.printStackTrace();
        }

    } 

